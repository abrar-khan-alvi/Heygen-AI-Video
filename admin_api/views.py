class AdminCreateStaffView(APIView):
    # ONLY SUPERUSERS CAN CREATE NEW ADMINS
    permission_classes = [IsSuperUser]
    
    def post(self, request):
        email = request.data.get('email')
        username = request.data.get('username')
        password = request.data.get('password')
        is_superuser = request.data.get('is_superuser', False)
        admin_permissions = request.data.get('admin_permissions', {})
        
        if not email or not username or not password:
            return Response(
                {"error": "Email, username, and password are required."}, 
                status=status.HTTP_400_BAD_REQUEST
            )
            
        if CustomUser.objects.filter(email=email).exists():
            return Response(
                {"error": "A user with this email already exists."}, 
                status=status.HTTP_400_BAD_REQUEST
            )
            
        if CustomUser.objects.filter(username=username).exists():
            return Response(
                {"error": "A user with this username already exists."}, 
                status=status.HTTP_400_BAD_REQUEST
            )
            
        try:
            if is_superuser:
                user = CustomUser.objects.create_superuser(
                    email=email,
                    username=username,
                    password=password
                )
            else:
                user = CustomUser.objects.create_user(
                    email=email,
                    username=username,
                    password=password,
                    is_staff=True,
                    is_email_verified=True,
                    admin_permissions=admin_permissions
                )
            
            return Response({
                "message": f"New Admin {user.email} successfully created.",
                "user": AdminUserSerializer(user).data
            }, status=status.HTTP_201_CREATED)
            
        except ValueError as e:
            return Response({"error": str(e)}, status=status.HTTP_400_BAD_REQUEST)
        except Exception as e:
            return Response(
                {"error": "An error occurred while creating the admin account."}, 
                status=status.HTTP_500_INTERNAL_SERVER_ERROR
            )

class AdminUpdateStaffPermissionsView(APIView):
    permission_classes = [IsSuperUser]
    throttle_classes = [AdminActionThrottle]
    
    def _update(self, request, pk):
        if not request.user.is_superuser:
            return Response(
                {"error": "Only superadmins can modify staff permissions."}, 
                status=status.HTTP_403_FORBIDDEN
            )
            
        try:
            user_to_update = CustomUser.objects.get(
                pk=pk
            )
            if not user_to_update.is_staff and not user_to_update.is_superuser:
                raise CustomUser.DoesNotExist
        except CustomUser.DoesNotExist:
            return Response({"error": "Admin user not found."}, status=status.HTTP_404_NOT_FOUND)
            
        # Prevent superadmins from accidentally revoking their own superuser status
        if request.user.id == user_to_update.id and 'is_superuser' in request.data and not request.data['is_superuser']:
            return Response({"error": "You cannot revoke your own superadmin status."}, status=status.HTTP_400_BAD_REQUEST)
            
        if 'is_superuser' in request.data:
            user_to_update.is_superuser = request.data['is_superuser']
            
        if 'admin_permissions' in request.data:
             # Merge with existing permissions or replace
             current_perms = user_to_update.admin_permissions or {}
             current_perms.update(request.data['admin_permissions'])
             user_to_update.admin_permissions = current_perms
             
        user_to_update.save(update_fields=['is_superuser', 'admin_permissions'])
        
        return Response({
            "message": "Permissions updated successfully", 
            "user": AdminUserSerializer(user_to_update).data
        }, status=status.HTTP_200_OK)

    def put(self, request, pk):
        return self._update(request, pk)

    def patch(self, request, pk):
        return self._update(request, pk)

class AdminDeleteStaffView(APIView):
    permission_classes = [IsSuperUser]
    
    def delete(self, request, pk):
        try:
            # Match superusers too — they may not have is_staff=True
            user = CustomUser.objects.get(
                pk=pk
            )
            if not user.is_staff and not user.is_superuser:
                raise CustomUser.DoesNotExist
        except CustomUser.DoesNotExist:
            return Response({"error": "Staff member not found."}, status=status.HTTP_404_NOT_FOUND)
            
        # Prevent a superadmin from deleting themselves
        if request.user.id == user.id:
            return Response({"error": "You cannot delete your own account."}, status=status.HTTP_400_BAD_REQUEST)
            
        user.delete()
        return Response({"message": "Staff member successfully deleted."}, status=status.HTTP_204_NO_CONTENT)


