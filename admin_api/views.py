class DashboardStatsView(APIView):
    permission_classes = [IsAdminUser]

    def get(self, request):
        now = timezone.now()
        thirty_days_ago = now - timedelta(days=30)
        
        # 1. User Stats (excluding admins and superusers)
        regular_users_qs = CustomUser.objects.filter(is_staff=False, is_superuser=False)
        total_users = regular_users_qs.count()
        new_users_30d = regular_users_qs.filter(date_joined__gte=thirty_days_ago).count()
        
        # 2. Subscription Stats
        active_subscriptions = UserSubscription.objects.filter(status=UserSubscription.Status.ACTIVE).count()
        trial_subscriptions = UserSubscription.objects.filter(status=UserSubscription.Status.TRIAL).count()
        
        # Aggregate Monthly Recurring Revenue (approximate based on active paid plans)
        mrr_data = UserSubscription.objects.filter(
            status=UserSubscription.Status.ACTIVE
        ).aggregate(total_mrr=Sum('plan__price_monthly'))
        approx_mrr = mrr_data['total_mrr'] or 0

        # 3. Video Stats
        total_videos = VideoProject.objects.count()
        completed_videos = VideoProject.objects.filter(status=VideoProject.StatusChoice.VIDEO_COMPLETED).count()
        failed_videos = VideoProject.objects.filter(status=VideoProject.StatusChoice.VIDEO_FAILED).count()
        videos_last_30d = VideoProject.objects.filter(
            status=VideoProject.StatusChoice.VIDEO_COMPLETED,
            updated_at__gte=thirty_days_ago
        ).count()

        # 4. Chart Data: Signups by Day (last 30 days)
        signups_by_day = list(
            CustomUser.objects.filter(date_joined__gte=thirty_days_ago)
            .extra({'date': "date(date_joined)"})
            .values('date')
            .annotate(count=Count('id'))
            .order_by('date')
        )
        
        # 5. Chart Data: Videos Generated by Day (last 30 days)
        videos_by_day = list(
            VideoProject.objects.filter(
                status=VideoProject.StatusChoice.VIDEO_COMPLETED,
                updated_at__gte=thirty_days_ago
            )
            .extra({'date': "date(updated_at)"})
            .values('date')
            .annotate(count=Count('id'))
            .order_by('date')
        )
        
        # Format chart data to ensure all dates are present (simplified for now)
        # In a real production app, you'd fill in the missing days with 0s.
        
        # 6. Chart Data: Subscriptions by Plan (Active/Trial only)
        subscriptions_by_plan = list(
            UserSubscription.objects.filter(
                status__in=[UserSubscription.Status.ACTIVE, UserSubscription.Status.TRIAL]
            )
            .values('plan__name')
            .annotate(count=Count('id'))
            .order_by('-count')
        )
        # Format for Recharts (needs 'name' and 'value')
        formatted_subscriptions_by_plan = [
            {"name": item['plan__name'], "value": item['count']} 
            for item in subscriptions_by_plan
        ]

        return Response({
            "overview": {
                "total_users": total_users,
                "new_users_30d": new_users_30d,
                "active_paid_subscriptions": active_subscriptions,
                "active_trial_subscriptions": trial_subscriptions,
                "approximate_mrr": approx_mrr,
                "total_videos_created": total_videos,
                "completed_videos": completed_videos,
                "completed_videos_30d": videos_last_30d,
                "failed_videos": failed_videos,
            },
            "charts": {
                "signups_by_day": signups_by_day,
                "videos_by_day": videos_by_day,
                "subscriptions_by_plan": formatted_subscriptions_by_plan,
            }
        })


